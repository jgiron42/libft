#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n / sizeof(long int)
		&& ((long int *)s1)[i] == ((long int *)s2)[i])
		i++;
	i *= sizeof(long int);
	while (i < n && ((char *)s1)[i] == ((char *)s2)[i])
		i++;
	if (i == n)
		return (0);
	return (((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]);
}
